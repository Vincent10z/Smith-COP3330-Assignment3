package EX46;

public class WordNum {

    private String word;
    private int count;

    public WordNum(String word, int count) {
        super();
        this.word = word;
        this.count = count;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public int getCount(int i) {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

}